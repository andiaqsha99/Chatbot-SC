def bag_of_words(s, words):
    bag = [0 for _ in range(len(words))]

    s_words = stopword.remove(s)
    s_words = [stemmer.stem(word.lower()) for word in s_words]

    for se in s_words:
        for i, w in enumerate(words):
            if w == se:
                bag[i] = 1
            
    return numpy.array(bag)


